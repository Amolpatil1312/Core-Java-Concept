package com.csi.switchcasecoreconcept;

public class Service {

	void add(int n1, int n2) {
		System.out.println("\n Addition Result: " + (n1 + n2));
	}

	void sub(int n1, int n2) {
		System.out.println("\n Substraction Result: " + (n1 - n2));
	}

	void mul(int n1, int n2) {
		System.out.println("\n Multiplication Result: " + (n1 * n2));
	}

	void div(int n1, int n2) {
		System.out.println("\n Division Result: " + (n1 / n2));
	}

}
